<?php

namespace Plugin\BarcodeForm\Entity;

class BarcodeForm extends \Eccube\Entity\AbstractEntity
{
    private $product_id;

    private $code;

    public function getProductId()
    {
        return $this->product_id;
    }

    public function setProductId($product_id)
    {
        $this->product_id = $product_id;

        return $this;
    }

    public function getCode()
    {
        return $this->code;
    }

    public function setCode($code)
    {
        $this->code = $code;

        return $this;
    }

}